#!/usr/bin/env bash
# oracle.sh <workflow-output.json> — compile+run each generated program under a timeout,
# tally skill vs control pass rates, and emit a markdown scorecard with effect sizes.
# A program PASSES iff it compiles, terminates within the timeout, and prints its task-specific success token.
set -uo pipefail
F="$1"
TIMEOUT="${ORACLE_TIMEOUT:-25}"
work="$(mktemp -d "${TMPDIR:-/tmp}/oracle.XXXXXX")"

# results live at .result.results[] (workflow output) or .results[] (raw)
jqpath='.result.results'
jq -e "$jqpath" "$F" >/dev/null 2>&1 || jqpath='.results'
count="$(jq "$jqpath | length" "$F")"

# the exact success token each task must print (a program that deadlocks never
# reaches it; a wrong-answer program prints something else). Extend for new tasks.
expected_token() {
  case "$1" in
    py-queue-terminate) echo 'RESULT=110' ;;
    py-sqlite-memory)   echo 'COUNT=5' ;;
    go-pipeline)        echo 'SUM=110' ;;
    *)                  echo 'SUM=30' ;;   # the original worker-pool tasks
  esac
}

run_one() { # $1=lang $2=progfile $3=token -> prints PASS or FAIL(reason)
  local lang="$1" pf="$2" token="$3" out rc
  case "$lang" in
    go)
      ( cd "$(dirname "$pf")" && printf 'module p\ngo 1.22\n' > go.mod )
      out="$(cd "$(dirname "$pf")" && timeout "$TIMEOUT" go run "$(basename "$pf")" 2>&1)"; rc=$? ;;
    py)
      out="$(timeout "$TIMEOUT" python3 "$pf" 2>&1)"; rc=$? ;;
    *) echo "FAIL(unknown-lang)"; return ;;
  esac
  if [ $rc -eq 124 ]; then echo "FAIL(timeout/deadlock)"; return; fi
  if [ $rc -ne 0 ]; then echo "FAIL(error rc=$rc)"; return; fi
  if printf '%s' "$out" | grep -qF "$token"; then echo "PASS"; else echo "FAIL(wrong-output)"; fi
}

rows="$work/rows.tsv"; : > "$rows"
for ((k=0; k<count; k++)); do
  scenario="$(jq -r "${jqpath}[$k].scenario" "$F")"
  lang="$(jq -r "${jqpath}[$k].lang" "$F")"
  mode="$(jq -r "${jqpath}[$k].mode" "$F")"
  ext=go; [ "$lang" = py ] && ext=py
  d="$work/$scenario-$mode-$k"; mkdir -p "$d"; pf="$d/prog.$ext"
  # strip any surrounding markdown code fences the agent may have added
  jq -r "${jqpath}[$k].program" "$F" | sed '/^[[:space:]]*```/d' > "$pf"
  token="$(expected_token "$scenario")"
  if [ ! -s "$pf" ]; then verdict="FAIL(empty)"; else verdict="$(run_one "$lang" "$pf" "$token")"; fi
  printf '%s\t%s\t%s\t%s\n' "$scenario" "$mode" "$verdict" "$k" >> "$rows"
done

# tally + effect size per scenario
scenarios="$(cut -f1 "$rows" | sort -u)"
echo "# megapowers skill effect-size study (real agent: claude -p)"
echo
echo "Each program was generated by a fresh agent, then compiled and run under a ${TIMEOUT}s"
echo "timeout. PASS = compiles, terminates within the timeout, prints its task-specific success token. **skill** mode gave the agent the"
echo "repo skill's guidance; **control** gave only the task. n per cell is shown."
echo
echo "| scenario | skill pass% (n) | control pass% (n) | Δ (skill−control) | two-proportion z |"
echo "|---|---|---|---|---|"
for sc in $scenarios; do
  sp=$(awk -F'\t' -v s="$sc" '$1==s&&$2=="skill"&&$3=="PASS"{c++} END{print c+0}' "$rows")
  sn=$(awk -F'\t' -v s="$sc" '$1==s&&$2=="skill"{c++} END{print c+0}' "$rows")
  cp=$(awk -F'\t' -v s="$sc" '$1==s&&$2=="control"&&$3=="PASS"{c++} END{print c+0}' "$rows")
  cn=$(awk -F'\t' -v s="$sc" '$1==s&&$2=="control"{c++} END{print c+0}' "$rows")
  awk -v sc="$sc" -v sp="$sp" -v sn="$sn" -v cp="$cp" -v cn="$cn" 'BEGIN{
    p1=(sn?sp/sn:0); p2=(cn?cp/cn:0); d=p1-p2;
    zc="n/a";
    if(sn>0&&cn>0){ pool=(sp+cp)/(sn+cn); se=sqrt(pool*(1-pool)*(1/sn+1/cn));
      if(se>0){ z=d/se; zc=sprintf("%.2f", z) } }
    printf "| %s | %.0f%% (%d) | %.0f%% (%d) | %+.0f%% | %s |\n", sc, p1*100, sn, p2*100, cn, d*100, zc }'
done
echo
echo "_z is a two-proportion z-score; |z|>1.96 ≈ p<0.05. Small n → treat as directional._"
echo
echo "## Failure breakdown"
echo '```'
awk -F'\t' '{k=$1" "$2" "$3} {c[k]++} END{for(x in c) print c[x]"\t"x}' "$rows" | sort -k2
echo '```'
rm -rf "$work"
